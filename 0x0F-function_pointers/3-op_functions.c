#include "calc.h"

/**
 * op_add - returns sum of 2 integers
 * @a: first int
 * @b: second int
 * Return: sums of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference of 2 integers
 * @a: first int
 * @b: second int
 * Return: difference of a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns product of 2 integers
 * @a: first int
 * @b: second int
 * Return: multiplies  a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns division of 2 integers
 * @a: first int
 * @b: second int
 * Return: division of  a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns remainder of division of 2 integers
 * @a: first int
 * @b: second int
 * Return: remainder of division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
